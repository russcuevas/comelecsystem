                    <p>REVISED CALENDAR OF ACTIVITIES AND PERIODS OF CERTAIN PROHIBITED ACTS IN CONNECTION WITH THE
                        OCTOBER 30, 2023
                        SYNCHRONIZED BARANGAY AND SANGGUNIANG KABATAAN ELECTIONS (BSKE).</p>
                    <p><span style="font-weight: 900;">WHEREAS,</span>
                        the Commission on Elections (Commission) has promulgated on March 22, 2023 Resolution No. 10902
                        entitled "REVISED
                        CALENDAR OF ACTIVITIES AND PERIODS OF CERTAIN PROHIBITED ACTS IN CONNECTION WITH THE OCTOBER 30,
                        2023 SYNCHRONIZED
                        BARANGAY AND SANGGUNIANG KABATAAN (SK) ELECTIONS" prescribing the activities, periods and
                        prohibited acts in connection
                        with the October 30, 2023 Synchronized Barangay and Sangguniang Kabataan Elections (BSKE);
                    </p>
                    <p><span style="font-weight: 900;">WHEREAS,</span>
                        under Resolution No. 10902, the period to constitute the members of the Electoral Board and
                        Barangay Board of Canvassers
                        (BBOC) is from August 1, 2023 (TUE) to October 11, 2023 (WED);
                    </p>
                    <p><span style="font-weight: 900;">WHEREAS,</span>
                        there is a need to give ample time for the conduct of training of the members of the Electoral
                        Board and Barangay Board
                        of Canvassers (BBOC);
                    </p>
                    <p><span style="font-weight: 900;">WHEREAS,</span>
                        Section 29 of Republic Act No. 6646 provides: <br>
                        "SEC. 29. Designation of Other Dates for Certain Pre-election Acts. - If it should no longer be
                        reasonably possible to
                        observe the periods and dates prescribed by law for certain pre-election acts, the Commission
                        shall fix other periods
                        and dates in order to ensure accomplishment of the activities so voters shall not be deprived of
                        their right of
                        suffrage.";
                    </p>
                    <p><span style="font-weight: 900;">NOW, THEREFORE,</span>
                        pursuant to the powers vested in it by the Constitution of the Republic of the Philippines, the
                        Omnibus Election Code
                        (B.P. Big. 881), Republic Act Nos. 6646 and other election laws, the Commission RESOLVED, as it
                        hereby RESOLVES, to
                        reset the last day for the constitution of the members of the Electoral Board and Barangay Board
                        of Canvassers (BBOC)
                        from October 11, 2023 (WED) to September 9, 2023 (SAT), thereby revising Resolution No. 10902,
                        as follows:
