                        <p>George Erwin M. Garcia <br>
                            <span style="font-style: italic;">Chairman</span>
                        </p>
                    </span>
                    <p>**Greetings and peace to all. Welcome to the Commission on Elections website.
                    </p>
                    <p>As the Comelec continues to strengthen its renewed relationship with and fortify trust given by
                        the Filipino masses, the
                        Comelec intends to have a fruitful engagement with its stakeholders and encourages deeper
                        involvement from the public by
                        doing our part to disseminate news and information and publish documented activities to keep
                        everyone updated.</p>
                    <p>Our website will highlight the Commission's official public engagements and collaborations as
                        well as its performance as
                        an administrative and quasi-judicial office, including posts on relevant laws, resolutions, and
                        updates on bidding,
                        procurement, preparations and conduct of electoral processes. This website will also feature The
                        Chairman's Corner which
                        will serve as an avenue to reflect the policy direction of this Institution under the leadership
                        of Chairman George
                        Erwin M. Garcia for the next seven years, and to share the Commission’s progress towards
                        transparency, professionalism,
                        and other institutional reforms through a modern and inclusive approach led by him.
                    </p>
                    <p>The Comelec is in solidarity with the Filipinos in safeguarding the sanctity of the elections and
                        in achieving a
                        well-informed electorate. It is Chairman Garcia’s fervent aspiration that the Comelec will be
                        known as the Institution
                        of a reassuring presence of an honest, peaceful and credible elections and a constant symbolism
                        of a vibrant and
                        flourishing democracy. In the face of various uncertainties in the years ahead, it is the
                        Chairman’s mission that this
                        Institution will continue to provide a platform that would build a conscious electorate equipped
                        with knowledge to elect
                        the right leaders. Thomas Jefferson once said, "a well-informed electorate is a prerequisite to
                        democracy".</p>
                    <p>This Commission understands the importance of keeping the Filipinos in the loop to be fully
                        attuned with its
                        constitutional mandate and to uphold its integrity. We are optimistic that through this website
                        the people will have an
                        overview about the Comelec and on how this Institution discharges its functions as the
                        independent constitutional body
                        primarily in charge of the administration of the conduct of elections.</p>
                    <p>Nawa’y maging mas mabisang tulay ito tungo sa mas mabuting pag-unawaan ng Komisyon at ng
                        Mamayanan. Maraming salamat po
                        at mabuhay po kayong lahat.</p>
